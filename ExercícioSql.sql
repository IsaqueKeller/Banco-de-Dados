DROP TABLE IF EXISTS ITEM_PEDIDO CASCADE;
DROP TABLE IF EXISTS PEDIDO CASCADE;
DROP TABLE IF EXISTS PRODUTO CASCADE;
DROP TABLE IF EXISTS CLIENTE CASCADE;
DROP TABLE IF EXISTS VENDEDOR CASCADE;

CREATE TABLE CLIENTE (
COD_CLIENTE INTEGER PRIMARY KEY,
NOME_CLIENTE VARCHAR (50) NOT NULL,
END_CLIENTE VARCHAR(100),
CID_CLIENTE VARCHAR (100),
CEP_CLIENTE VARCHAR (30),
UF_CLIENTE VARCHAR (2),
CNPJ_CLIENTE VARCHAR(20),
INSCR_ESTADUAL_CLIENTE VARCHAR(50)
);

CREATE TABLE VENDEDOR (
COD_VENDEDOR INTEGER PRIMARY KEY,
NOME_VENDEDOR VARCHAR(40) NOT NULL,
SALARIO_FIXO NUMERIC (8,2) NOT NULL,
FAIXA_DE_COMISSAO NUMERIC (6,2)
);

CREATE TABLE PRODUTO (
NUM_PRODUTO INTEGER PRIMARY KEY,
UNIDADE_PRODUTO INTEGER NOT NULL,
DESCRICAO_PRODUTO VARCHAR(100),
VALOR_UNITARIO NUMERIC (8,2)
);

CREATE TABLE PEDIDO (
NUM_PEDIDO INTEGER PRIMARY KEY,
PRAZO_ENTREGA_PEDIDO VARCHAR (30) NOT NULL,
COD_CLIENTE INTEGER,
COD_VENDEDOR INTEGER 
);

CREATE TABLE ITEM_PEDIDO (
NUM_PEDIDO INTEGER,
NUM_PRODUTO INTEGER,
QUANT_PEDIDA INTEGER NOT NULL,
PRIMARY KEY (NUM_PEDIDO , NUM_PRODUTO)
);

ALTER TABLE ITEM_PEDIDO 
ADD FOREIGN KEY (NUM_PRODUTO)
REFERENCES PRODUTO(NUM_PRODUTO);

ALTER TABLE ITEM_PEDIDO
ADD FOREIGN KEY (NUM_PEDIDO)
REFERENCES PEDIDO(NUM_PEDIDO);

ALTER TABLE PEDIDO
ADD FOREIGN KEY (COD_CLIENTE)
REFERENCES CLIENTE(COD_CLIENTE);

ALTER TABLE PEDIDO
ADD FOREIGN KEY (COD_VENDEDOR)
REFERENCES VENDEDOR(COD_VENDEDOR);

ALTER TABLE PEDIDO 
ALTER COLUMN COD_CLIENTE SET NOT NULL,
ALTER COLUMN COD_VENDEDOR SET NOT NULL;

ALTER TABLE ITEM_PEDIDO
ALTER COLUMN NUM_PEDIDO SET NOT NULL,
ALTER COLUMN NUM_PRODUTO SET NOT NULL;

